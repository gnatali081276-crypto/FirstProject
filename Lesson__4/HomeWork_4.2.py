# === ДЗ 4.2 : Знайти суму елементів із парними індексами ===

nums = [0, 1, 7, 2, 4, 8]

if len(nums) == 0:
    result = 0

else:
    even_sum = sum(nums[::2])

    result = even_sum * nums[-1]

print(result)

# Приклади:
#[0, 1, 7, 2, 4, 8] => (0 + 7 + 4) * 8 = 88

#[1, 3, 5] => 30
#[6] => 36
#[] => 0
