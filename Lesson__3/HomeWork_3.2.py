# === ДЗ 3.2: Перемістити елемент у списку ===

nums = [12, 3, 4, 10, 8]
if len(nums) > 1:

    last = nums.pop()

    nums.insert(0, last)

print(nums)


# Приклади:
# [12, 3, 4, 10] => [10, 12, 3, 4]
# [1] => [1]
# [] => []
# [12, 3, 4, 10, 8] => [8, 12, 3, 4, 10]
# усі приклади перевірила, вони працюють